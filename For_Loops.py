# for loop = a statement that will execute its block of code a limited amount of times 
#
#           while loop = unlimited 
#           for loop = limited

for letter in "Giraffe Academy":
    print(letter)

friends = ["Jim", "Karen", "Kevin"]
for name in friends:
    print(name)

friends = ["Jim", "Karen", "Kevin"]
len(friends)
for index in range(len(friends)):
    print(friends[index])